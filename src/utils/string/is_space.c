#include "string.h"

bool	is_space(int c)
{
	bool	is;

	is = false;
	is |= (c == '\f');
	is |= (c == '\n');
	is |= (c == '\r');
	is |= (c == '\t');
	is |= (c == '\v');
	is |= (c == ' ');
	return (is);
}
