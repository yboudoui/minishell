#include "string.h"

bool	is_alpha(char c)
{
	int	is_upper;
	int	is_lower;

	is_upper = (c >= 'A' && c <= 'Z');
	is_lower = (c >= 'a' && c <= 'z');
	return (is_upper || is_lower);
}
